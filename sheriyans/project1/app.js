const cookieParser = require('cookie-parser');
const express=require('express');
const app=express();
const userModel=require("./models/user");
const postModel=require("./models/post");
const path=require("path");
const bcrypt=require("bcrypt");
const jwt=require('jsonwebtoken');
const PORT=3000;
 
app.set("view engine","ejs");
app.use(express.json());
app.use(express.urlencoded({extended:true}));
app.use(cookieParser());
app.use(express.static(path.join(__dirname,"public")));

app.get('/',(req,res)=>{
    res.render('index');
})

app.get('/profile',isLoggerIn,async (req,res)=>{
    // console.log(req.user);
    let user=await userModel.findOne({email:req.user.email}).populate("posts");
    res.render('profile',{user});
})

app.get('/like/:id',isLoggerIn,async (req,res)=>{
    let post=await postModel.findOne({_id:req.params.id}).populate("user");

    if(post.likes.indexOf(req.user.userid)=== -1 ){
        post.likes.push(req.user.userid);
    }
    else{
        post.likes.splice(post.likes.indexOf(req.user.userid),1);
    }

    await post.save();
    res.redirect('/profile');
})

app.get('/edit/:id',isLoggerIn,async (req,res)=>{
    let post=await postModel.findOne({_id:req.params.id}).populate("user");

    res.render('edit', {post})
})

app.post('/post',isLoggerIn,async (req,res)=>{
    let user=await userModel.findOne({email:req.user.email});

    let {content}=req.body;

    let post=await postModel.create({
        user:user._id,
        content,
    })

    user.posts.push(post._id);
    await user.save();
    res.redirect('/profile');
})


app.post('/update/:id',isLoggerIn,async (req,res)=>{
    let post=await postModel.findOneAndUpdate({_id:req.params.id},{content:req.body.content});
    res.redirect("/profile")
})

app.post("/register",async (req,res)=>{
    let {username,name,age,email,password}=req.body;

    let user= await userModel.findOne({email});

    if(user) return res.status(500).send("User with this email already exist");

    bcrypt.genSalt(10,(err,salt)=>{
        bcrypt.hash(password,salt,async (err,hash)=>{
            let createuser=await userModel.create({
                username,
                name,
                age,
                email,
                password:hash
            });

            let token=jwt.sign({email:email,userid:createuser._id},"akanksha");
            res.cookie("token",token);

            res.send("Registered User");
        })
    })
})

app.get("/login",(req,res)=>{
    res.render("login");
})

app.post("/login",async (req,res)=>{
    let {username,name,age,email,password}=req.body;

    let user= await userModel.findOne({email});

    if(!user) return res.status(500).send("Something went wrong");

    bcrypt.compare(password,user.password,(err,result)=>{
        if(result) {
            let token=jwt.sign({email:email,userid:user._id},"akanksha");
            res.cookie("token",token);
            res.status(200).redirect("/profile");
        }
        else res.redirect("/login");
    })

})

app.get("/logout",(req,res)=>{
    res.cookie("token","");
    res.redirect("/login");
})

function isLoggerIn(req,res,next){
    if(req.cookies.token==="") res.redirect("/login");
    else{
        let data=jwt.verify(req.cookies.token,"akanksha");
        req.user=data;
        next();
    }
}

app.listen(PORT,()=>{
    console.log(`App running on port :${PORT}`);
})
